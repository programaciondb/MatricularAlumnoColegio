    private void generarMatriculaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarMatriculaButtonActionPerformed

        
        if (!matriculaGenerada){
            try {
                // TODO add your handling code here:
                numMatricula = Controlador.generarMatricula(conexion);
                matriculaGenerada = true;
                matriculaTextField.setText(String.valueOf(numMatricula));
                matriculaTextField.setEditable(false);
            } catch (SQLException ex) {
                System.out.println(ex.getMessage());
            } 
        } else {
            JOptionPane.showMessageDialog(null,"Nº de matricula ya generado. Completar datos del alumno para matricular","Matricula existe", JOptionPane.DEFAULT_OPTION);
        }


    }//GEN-LAST:event_generarMatriculaButtonActionPerformed

