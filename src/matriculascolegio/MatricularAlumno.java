    private void cancelarMatriculaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelarMatriculaButtonActionPerformed
        // TODO add your handling code here:
        if (matriculaGenerada) {
        Controlador.cancelarMatricula(conexion, numMatricula);
        matriculaGenerada = false;
        JOptionPane.showMessageDialog(null,"Matricula "+numMatricula+" eliminada del sistema");
        numMatricula = 0;
        matriculaTextField.setText(String.valueOf(numMatricula));
        } else {
            JOptionPane.showMessageDialog(null, "Ninguna matricula creada para cancelar. Debes generar nueva primero");
        }


    }//GEN-LAST:event_cancelarMatriculaButtonActionPerformed

