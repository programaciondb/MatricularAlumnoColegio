    public void generarDiasComboBox(int idMes, int año) {
        idMes +=1;
        int i;
        boolean bisiesto;
        bisiesto = año % 4 == 0;
        
        switch(idMes) {
            case 1:
                i = 31;
                break;
            case 2:
                i = 28;
                if(bisiesto) i = 29;
                break;
            case 3:
                i = 31;   
                break;
            case 4:
                i = 30;
                break;
            case 5:
                i = 31;
                break;
            case 6:
                i = 30;
                break;
            case 7:
                i = 31;
                break;
            case 8:
                i = 31;
                break;
            case 9:
                i = 30;
                break;
            case 10:
                i = 31;
                break;
            case 11:
                i = 30;
                break;
            case 12:
                i = 31;
                break;
            default:
                i = 0;
        }
        
        diaFechaComboBox.removeAllItems();
        
        for(int j=0;j<i;j++){
            diaFechaComboBox.addItem(String.valueOf(j+1));
        }
        
        
        
    }
    
    public void matricularBoton() {
        Alumno alumno = Alumno.getInstance();
        boolean isNull = true;
        int idMatricula;
        String nombre, apellido, fechaNac, nombreApoderado, correo, direccion, idCurso, rut, telefono;
        char sexo;
        
        
        idMatricula = numMatricula;
        if(idMatricula != 0) {
            alumno.setIdMatricula(idMatricula);
        } else {
            isNull = true;
        }
        
        nombre = nombreTextField.getText();
        if(!nombre.isEmpty()){
            alumno.setNombre(nombre);
        }
        
        apellido = apellidoTextField.getText();
        if(!apellido.isEmpty()){
            alumno.setApellido(apellido);
        }
       
        rut = rutTextField.getText();
        if(!rut.isEmpty()){
            alumno.setRut(Integer.parseInt(rut));
        }
        
        nombreApoderado = nombreApoderadoTextField.getText();
        if (!nombreApoderado.isEmpty()) {
            alumno.setNombreApoderado(nombreApoderado);
        }
        
        fechaNac = añoFechaComboBox.getSelectedItem().toString()+"-"+(mesFechaComboBox.getSelectedIndex()+1)+"-"+diaFechaComboBox.getSelectedItem().toString();
        alumno.setFechaNac(fechaNac);
        
        if (sexoComboBox.getSelectedIndex() == 0) {
            sexo = "F".charAt(0);
        } else {
            sexo = "M".charAt(0);
        }
        alumno.setSexo(sexo);
        
        correo = correoTextField.getText();
        if (!correo.isEmpty()) {
            alumno.setCorreo(correo);
        }
        
        telefono = telefonoTextField.getText();
        if (!telefono.isEmpty()) {
            alumno.setTelefono(Integer.parseInt(telefono));
        }
        
        direccion = direccionTextField.getText();
        if (!direccion.isEmpty()) {
            alumno.setDireccion(direccion);
        }
        
        switch(cursosComboBox.getSelectedIndex()){
            case 0:
                idCurso = "PB";
                break;
            case 1:
                idCurso = "1T";
                break;
            case 2: 
                idCurso = "2T";
                break;
            case 3: 
                idCurso = "3T";
                break;
            case 4:
                idCurso = "4T";
                break;
            case 5:
                idCurso = "5T";
                break;
            case 6: 
                idCurso = "6T";
                break;
            case 7: 
                idCurso = "7T";
                break;
            case 8:
                idCurso = "8T";
                break;
            case 9:
                idCurso = "11T";
                break;
            case 10:
                idCurso = "12T";
                break;
            case 11:
                idCurso = "13T";
                break;
            case 12:
                idCurso = "14T";
                break;
            default:
                idCurso = "1T";
        }
        
        alumno.setIdCurso(idCurso);
        
        /*
        int idMatricula;
        String nombre, apellido, fechaNac, nombreApoderado, correo, direccion, idCurso, rut, telefono;
        char sexo;
        */
        isNull = (nombre.equals("") | apellido.equals("") | nombreApoderado.equals("") | correo.equals("") | direccion.equals("") | rut.equals("") | telefono.equals("") );
        if (!isNull) {
            Controlador.matricularAlumno(conexion, alumno);
        } else {
            JOptionPane.showMessageDialog(null, "Faltan datos para matricular.");
        }
        
    }
    
    
